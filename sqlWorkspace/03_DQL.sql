SELECT R.REVIEW_NO REVIEW_NO, R.MEMBER_NO MEMBER_NO, M.NAME MEMBER_NAME, R.PRODUCT_NO, R.RATING, R.CONTENT, R.CREATED_DATE, R.DELETED_YN
FROM REVIEW R
JOIN MEMBER M ON R.MEMBER_NO = M.MEMBER_NO
